CREATE DATABASE scandiweb3;

use scandiweb3;

CREATE TABLE `product_s` (
 `id` INT NOT NULL auto_increment,
 `sku` VARCHAR(255) NOT NULL,
 `n_ame` VARCHAR(255) NOT NULL,
 `price` VARCHAR(255) NOT NULL,
 `size` VARCHAR(255),
 `w_eight` VARCHAR(255),
 `dimension` varchar(255),
  PRIMARY KEY  (`id`)
);